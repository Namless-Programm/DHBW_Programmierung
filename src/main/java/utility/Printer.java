package utility;

/** 
 * Console Printer
 */

public class Printer {
	
	/**
	 * Prints "Hello World"
	 */
	
	public static void printHelloWorld() {
		System.out.println("Hello World");
	}
	
	/**
	 * Prints the incoming text
	 * @param text incoming text
	 */
	public static void printText(String text) {
		System.out.println(text);
	}
}
